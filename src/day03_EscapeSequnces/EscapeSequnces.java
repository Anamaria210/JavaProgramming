package day03_EscapeSequnces;

public class EscapeSequnces {
    public static void main(String[] args) {

        System.out.println("Java \nPythonc \nC#");

        System.out.println("...............");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all !\nWhat class do we have next week ?");


        System.out.println("..................................");
        System.out.println("\tJava is a cool programming language");
        System.out.println("..................................");
        System.out.println("/ \\"); //in order to print one backward slash we need to give two backslash
        System.out.println("my favourite tv show is \"Game of Throne\""); //in order to print double quote we need use \"


    }

}




/*
escape sequences : Must be given with double quote
    \n starts a new line
    /t tab paragraph space
    \\ one backslash
    \" double quote
 */

